extern const unsigned char font[];

struct dotMatrix_s {
	/* Display data buffer */
	uint8_t* data;
	/* Max number of lines can be supported by data, i.e. the size of data. */
	uint32_t maxLine;
	/* Number of lines to display */
	int32_t numLine;
};

int32_t dotMatrix_create(dotMatrix_t** ppSelf, uint32_t numLine) {
	dotMatrix_t *pSelf = NULL;

	pSelf = (dotMatrix_t*) malloc(sizeof(dotMatrix_t));
	if (NULL == pSelf) {
		return STATUS_ERROR_MALLOC;
	}

	pSelf->data = (uint8_t*) calloc(numLine, sizeof(uint8_t));
	if (NULL == pSelf->data) {
		dotMatrix_destroy(&pSelf);
		return STATUS_ERROR_MALLOC;
	}
	pSelf->maxLine = numLine;
	pSelf->numLine = 0;

	*ppSelf = pSelf;
	return STATUS_OK;
}

int32_t dotMatrix_destroy(dotMatrix_t** ppSelf) {
	dotMatrix_t *pSelf = *ppSelf;

	if (NULL != pSelf) {
		if (NULL != pSelf->data) {
			free(pSelf->data);
		}
		free(pSelf);
		*ppSelf = NULL;
	}

	return STATUS_OK;
}

void dotMatrix_draw(dotMatrix_t* pSelf, int32_t xOffset, const uint8_t *data, uint32_t len) {
	int32_t num;

	if (xOffset < 0) {
		num = len + xOffset;

		if (num > 0) {
			/* data + abs(xOffset) == data - xOffset */
			memcpy(pSelf->data, data - xOffset, num);

			pSelf->numLine = num;
		}
	} else {
		num = pSelf->maxLine - xOffset;

		if (num > 0) {
			num = num < len? num : len;
			memcpy(pSelf->data + xOffset, data, num);

			pSelf->numLine = pSelf->maxLine;
		}
	}
}

void dotMatrix_drawChar(dotMatrix_t* pSelf, int32_t xOffset, char c) {
	const uint8_t *pData;
	uint8_t blank[DOTMATRIX_FONT_CHAR_MARGIN] = {0};

	pData = font + (c*DOTMATRIX_FONT_WIDTH);

	dotMatrix_draw(pSelf, xOffset, pData, DOTMATRIX_FONT_WIDTH);
	dotMatrix_draw(pSelf, xOffset + DOTMATRIX_FONT_WIDTH, blank, DOTMATRIX_FONT_CHAR_MARGIN);
}

void dotMatrix_drawText(dotMatrix_t* pSelf, int32_t xOffset, const char* text) {
	char c;
	while('\0' != *text) {
		c = *text;
		if ('\n' == c || '\r' == c) {
			c = ' ';
		}

		dotMatrix_drawChar(pSelf, xOffset, c);
		xOffset += DOTMATRIX_FONT_CHAR_WIDTH;
		text++;
	}

	pSelf->numLine = xOffset;
}

void dotMatrix_setNumLineToDisplay(dotMatrix_t* pSelf, uint32_t endLine) {
	pSelf->numLine = endLine;
	if (pSelf->numLine > pSelf->maxLine) {
		pSelf->numLine = pSelf->maxLine;
	}
}

void dotMatrix_getDisplay(dotMatrix_t* pSelf, int32_t xOffset, uint32_t len, uint8_t isWrap, uint8_t *data) {
	uint32_t idx, len1, len2;

	if (xOffset >= 0) {
		idx = (xOffset + len) < pSelf->numLine? (xOffset + len) : pSelf->numLine;
		len1 = idx - xOffset;
		len2 = len - len1;

		memcpy(data, &pSelf->data[xOffset], len1);

		if (isWrap) {
			/* Multiple wrapping in case len is a lot longer than pSelf->numLine */
			while (len2 > pSelf->numLine) {
				memcpy(data + len1, pSelf->data, pSelf->numLine);
				len2 -= pSelf->numLine;
			}
			memcpy(data + len1, pSelf->data, len2);
		} else {
			memset(data + len1, 0, len2);
		}
	} else {
		len1 = -xOffset;
		len1 = len1 < len? len1 : len;
		len2 = len - len1;

		if (isWrap) {
			memcpy(data, pSelf->data + pSelf->numLine - len1, len1);
		} else {
			memset(data, 0, len1);
		}

		/* Recursion to fill in the data. Now that we have filled the negative
		 * index/offset, the rest is non-negative index/offset, so
		 * call the function itself.
		 */
		dotMatrix_getDisplay(pSelf, 0, len2, isWrap, data + len1);
	}
}

void dotMatrix_clear(dotMatrix_t* pSelf){
	memset(pSelf->data, 0, pSelf->maxLine * MAX7219_DIGIT_LENGTH);
}

uint32_t dotMatrix_getNumLineToDisplay(dotMatrix_t* pSelf) {
	return pSelf->numLine;
}

uint32_t dotMatrix_getMaxLine(dotMatrix_t* pSelf) {
	return pSelf->maxLine;
}


